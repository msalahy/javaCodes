
package inherit;


public class Ceilling {
    private int height;
    private int paintedColor;

    public Ceilling(int height, int paintedColor) {
        this.height = height;
        this.paintedColor = paintedColor;
    }

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
    
    
    
}
